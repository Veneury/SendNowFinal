public class Prueba {
   private int num1,num2;
   
   private int suma(int num1, int num2)
   {
      this.num1=num1;
      this.num2=num2;
      return num1 + num2;
   }
   
      private int resta(int num1, int num2)
   {
      this.num1=num1;
      this.num2=num2;
      return num1 - num2;
   }
   
}
